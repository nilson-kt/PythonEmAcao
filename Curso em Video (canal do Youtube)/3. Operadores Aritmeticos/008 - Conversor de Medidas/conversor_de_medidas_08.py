"""
Programa que lê um valor em metros e o exibe convertido em centímetros e em milímetros.
"""

valor = float(input('Digite um valor em metros: '))
print(f'A conversão de {valor} metros para centímetros fica: {valor*100} cm')
print(f'A conversão de {valor} metros para milímetros fica: {valor*1000} mm')
